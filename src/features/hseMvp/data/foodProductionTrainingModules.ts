const optionId = (index: number) => String.fromCharCode(97 + index);

const toLegacyCards = (blocks: any[], version: string) =>
  blocks
    .filter((block) => block.type === 'video_card')
    .map((block, index) => ({
      id: block.id,
      title: block.title,
      shortText: block.scene,
      ruleCode: `SCENE-${index + 1}`,
      riskTag: block.competency || 'практическое действие',
      image: '',
      altText: block.visualDescription,
      version,
    }));

const toLegacyQuestions = (questions: any[]) =>
  questions.map((question) => ({
    id: question.id,
    type: 'single',
    text: question.question,
    relatedRuleCode: question.competency,
    riskTag: question.competency,
    recommendationRule: question.explanation,
    options: question.options.map((option: string, index: number) => ({
      id: optionId(index),
      text: option,
      correct: option === question.correctAnswer,
    })),
  }));

const buildModule = (module: any) => ({
  ...module,
  estimatedMinutes: module.durationMinutes,
  status: 'published',
  progress: 0,
  cards: toLegacyCards(module.blocks, module.version),
  questions: toLegacyQuestions(module.finalTest),
});

